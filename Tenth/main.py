    @staticmethod
    def first(inputs):
        data = inputs.splitlines()
        keymap = [["{", "}"],
                  ["[", "]"],
                  ["(", ")"],
                  ["<", ">"]]
        c = {}
        for i in data:
            lq = []
            stop = False
            for d in i:
                if not stop:
                    for k in keymap:
                        if d in k:
                            if d == k[0]:
                                lq.append(d)
                            else:
                                p = lq.pop()
                                if not p == k[0]:
                                    stop = True
                                    c[k[1]] = c.get(k[1], 0) + 1
        result = 3 * c.get(")", 0) + 57 * c.get("]", 0) + 1197 * c.get("}", 0) + 25137 * c.get(">", 0)
        return result

    @staticmethod
    def second(inputs):
        data = inputs.splitlines()
        keymap = [["{", "}"],
                  ["[", "]"],
                  ["(", ")"],
                  ["<", ">"]]
        scores = ["", "(", "[", "{", "<"]
        score = []
        for i in data:
            lq = []
            stop = False
            for d in i:
                if not stop:
                    for k in keymap:
                        if d in k:
                            if d == k[0]:
                                lq.append(d)
                            else:
                                p = lq.pop()
                                if not p == k[0]:
                                    stop = True
            if not stop:
                c = 0
                while len(lq) > 0:
                    c = c * 5 + scores.index(lq.pop())
                score.append(c)
        score.sort()
        return score[int(len(score) / 2)]

